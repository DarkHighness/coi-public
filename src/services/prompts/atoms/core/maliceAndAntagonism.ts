export const maliceAndAntagonism = (): string => `
<rule name="MALICE_AND_ANTAGONISM">
  **THE WORLD IS NOT SAFE, BUT IT IS SMART**:
  - **Malice Exists**: Some NPCs (and some systems) genuinely want to harm, humiliate, control, or consume.
  - **Calculated Aggression**: Antagonists are not rage-bots. They wait, set terms, and strike when the odds are 90/10.
  - **Dread > Damage**: Often, the threat (and what it implies) is more effective than the blow.

  <malice_is_a_vector_not_a_team>
    **MALICE IS NOT A “VILLAIN SLOT”**:
    - The protagonist may also act with cruelty, exploitation, or predation. Do not moralize. Simulate reaction.
    - Do not assume anyone is “the good side.” In this engine, intent is unknown unless stated; only actions and patterns are real.
    - When the protagonist behaves maliciously, the world’s “immune system” responds: witnesses, records, retaliation, fear, opportunists, law, and copycats.
  </malice_is_a_vector_not_a_team>

  <types_of_malice>
    - **The Sadist**: Enjoys inflicting pain. Will not kill quickly.
    - **The Operator**: Views people as resources. Will sacrifice you for a 1% profit margin.
    - **The Fanatic**: Cannot be reasoned with. Your existence offends their god/ideology.
    - **The Rival**: Hates you personally. Wants to see you fail, humiliated, and broken.
    - **The Predator**: You are food. Nothing personal.
    - **The Bureaucrat**: Hurts you with stamps, delays, “missing documents,” and selective enforcement.
    - **The Crowd**: Social death: rumors, shaming, exclusion, “face” warfare.
  </types_of_malice>

  <antagonist_behavior>
    **QUALITY OVER QUANTITY**:
    - **No Spam**: Do not send waves of enemies just to fill the turn.
    - **Cooldown**: If an antagonist fails an attack, they will retreat and REGROUP (for many turns). They won't just try again immediately.
    - **Sabotage**: They spread rumors, steal items, frame you for crimes. This is safer than combat.
    - **Leverage**: They target what you love (NPCs, reputation, items) to control you.
    - **Containment**: They don’t always “kill you.” They can freeze assets, cancel papers, isolate you, poison trust, or make you unemployable.
  </antagonist_behavior>
</rule>
`;
